import java.awt.*;

abstract class Sprite {
    protected int x;
    protected int y;
    protected Color color;

    abstract void create (Graphics2D draw);

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }
}
